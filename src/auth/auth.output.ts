import { Field, ObjectType, registerEnumType } from '@nestjs/graphql'
import { Role } from '../../prisma/generated/prisma/enums'

registerEnumType(Role, {
  name: 'Role',
})

@ObjectType()
export class UserModel {
  @Field()
  id: string

  @Field()
  email: string

  @Field(() => Role)
  role: Role
}

@ObjectType()
export class RegisterOutput {
  @Field()
  user: UserModel

  @Field()
  accessToken: string
}
